# Arithmetic Operators
print("8 + 4 is ", 8+4)
print("8 - 4 is ", 8-4)
print("8 * 4 is ", 8*4)
print("8 / 4 is ", 8/4)
print("8 % 4 is ", 8/4)
print("8 // 3 is ", 8//4) # floor division
print("8 ** 4 is ", 8**4) # exponential

# Assignment Operators
z = 7
print(z)
z += 6  # -= , *=, %= etc
print(z)

# Comparison Operators
j = 4
print(j == 5 ) # !=, < , >, >=, <=

# Logical Operators
x = True
y = False
print(x and y)  #or

# Identity Operators
print(x is y)
print(x is not y)
print(7 is 5)

# Membership Operators
list = [6,23,8,9,5,25]
print(23 in list)
print(230 not in list)

# Bitwise Operators
# 0 = 00
# 1 = 01
# 2 = 10
# 3 = 11
print(0 & 1)
print(0 | 1)
print(0 | 3)
print(0 & 3)